    private void graphQuantityJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_graphQuantityJButtonActionPerformed
        // TODO add your handling code here:

        DefaultCategoryDataset dataSet = new DefaultCategoryDataset();
        for (Product product : supplierEnterprise.getProductCatalog().getProductDirectory()) {
            dataSet.setValue(product.getSoldQuantity() - product.getAvailability(), product.getName(), product.getName());
        }
        JFreeChart chart = ChartFactory.createBarChart3D("Supplier Product sales details(Quantity basis)", "Products", "Sales Quantity", dataSet, PlotOrientation.VERTICAL, true, true, false);
        chart.setBackgroundPaint(Color.gray);
        chart.getTitle().setPaint(Color.green);
        CategoryPlot p = chart.getCategoryPlot();
        p.setRangeGridlinePaint(Color.BLACK);
        ChartFrame cf = new ChartFrame("Bar chart for Supplier", chart);
        cf.setVisible(true);
        cf.setSize(450, 350);
    }//GEN-LAST:event_graphQuantityJButtonActionPerformed

    
    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) productJTable.getModel();

        dtm.setRowCount(0);

        for (Product product : supplierEnterprise.getProductCatalog().getProductDirectory()) {
            int soldQuantity = product.getSoldQuantity() - product.getAvailability();
            Object[] row = new Object[6];
            row[0] = product;
            row[1] = product.getProductId();
            row[2] = product.getPrice();
            row[3] = product.getAvailability();
            row[4] = soldQuantity;
            row[5] = soldQuantity * product.getPrice();

            dtm.addRow(row);
        }

    }

    private void productQuantityWise() {
        Product maxProduct = null;
        Product salesProduct = null;
        for (Product product : supplierEnterprise.getProductCatalog().getProductDirectory()) {
            if (maxProduct == null) {
                maxProduct = product;
            }

            int maxQuantity = maxProduct.getSoldQuantity() - maxProduct.getAvailability();
            int productQuantity = product.getSoldQuantity() - product.getAvailability();

            if (maxQuantity < productQuantity) {
                maxProduct = product;
            }

            if (salesProduct == null) {
                salesProduct = product;
            }

            double maxSale = (salesProduct.getSoldQuantity() - salesProduct.getAvailability()) * salesProduct.getPrice();
            double prodSales = (product.getSoldQuantity() - product.getAvailability()) * product.getPrice();

            if (maxSale < prodSales) {
                salesProduct = product;
            }
        }

        if (salesProduct != null && ((salesProduct.getSoldQuantity() - salesProduct.getAvailability()) != 0)) {
            maxRevProductJTextField.setText(salesProduct.getName());
        }

        if (maxProduct != null && (maxProduct.getSoldQuantity() - maxProduct.getAvailability()) != 0) {
            maxSellingProductTextField.setText(maxProduct.getName());
        }
    }

