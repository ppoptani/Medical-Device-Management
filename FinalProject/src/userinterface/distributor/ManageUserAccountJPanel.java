    private void addUserAccountJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addUserAccountJButtonActionPerformed
        // TODO add your handling code here:
        //get the employee
        if (validateFields()) {

            Organization organization = (Organization) chooseOrgJComboBox.getSelectedItem();
            Employee employee = (Employee) chooseEmpJComboBox.getSelectedItem();
            Role role = (Role) chooseRoleJComboBox.getSelectedItem();

            UserAccount userAccount = createUserAccount(organization);
            userAccount.setEmployee(employee);
            if (activeJRadioButton.isSelected()) {
                userAccount.setStatus("Active");
            } else {
                userAccount.setStatus("Disable");
            }

            JOptionPane.showMessageDialog(null, role + " Added Successfully");
            refreshTable();
        } else {
            return;
        }
    }//GEN-LAST:event_addUserAccountJButtonActionPerformed

    private UserAccount createUserAccount(Organization organization) {
        UserAccount userAccount = organization.getUserAccountDirectory().createUserAccount();
        userAccount.setPassword(passwordJTextField.getText());
        userAccount.setUserName(userNameJTextField.getText());
        userAccount.setRole((Role) chooseRoleJComboBox.getSelectedItem());
        return userAccount;
    }

    private void chooseOrgJComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chooseOrgJComboBoxActionPerformed
        // TODO add your handling code here:
        Organization organization = (Organization) chooseOrgJComboBox.getSelectedItem();
        if (organization != null) {
            populateEmpComboBox(organization);
            populateRoleComboBox(organization);
        }
    }//GEN-LAST:event_chooseOrgJComboBoxActionPerformed

    private boolean validateFields() {
        boolean isValid = true;
        if (isStringTextFieldValid(userNameJTextField)) {
            errorUserNameJLabel.setText("Name contains only alphabest and numbers");
            isValid = false;
        } else {
            if (business.isUserNamePresent(userNameJTextField.getText().trim())) {
                errorUserNameJLabel.setText("User name already exist.");
                isValid = false;
            } else {
                errorUserNameJLabel.setText("");
            }
        }

        if (passwordJTextField.getText().isEmpty() || !(passwordJTextField.getText().matches("^\\s*[a-zA-Z0-9,\\s]+\\s*$"))) {
            errorPwdJLabel.setText("Password contains only alphabest and numbers");
            isValid = false;
        } else {
            if (passwordJTextField.getText().length() < 6) {
                errorPwdJLabel.setText("Password should be of 6 character");
                isValid = false;
            } else {
                errorPwdJLabel.setText("");
            }
        }

        if (chooseOrgJComboBox.getSelectedIndex() == -1) {
            isValid = false;
        }

        if (chooseEmpJComboBox.getSelectedIndex() == -1) {
            isValid = false;
        }

        if (chooseRoleJComboBox.getSelectedIndex() == -1) {
            isValid = false;
        }

        return isValid;
    }


