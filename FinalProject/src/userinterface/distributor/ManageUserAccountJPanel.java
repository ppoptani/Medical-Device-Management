    public void refreshTable() {
        int rowCount = userAccountJTable.getRowCount();
        DefaultTableModel model = (DefaultTableModel) userAccountJTable.getModel();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }

        for (Organization organization : organizationDirectory.getOrganizations()) {
            for (UserAccount ua : organization.getUserAccountDirectory().getUserAccounts()) {
                Object row[] = new Object[4];
                row[0] = ua;
                row[1] = ua.getPassword();
                row[2] = ua.getRole();
                row[3] = organization;
                model.addRow(row);
            }
        }

    }

    private void populateOrgComboBox() {
        chooseOrgJComboBox.removeAllItems();

        for (Organization organization : organizationDirectory.getOrganizations()) {
            chooseOrgJComboBox.addItem(organization);
        }
    }

    private void populateEmpComboBox(Organization organization) {
        chooseEmpJComboBox.removeAllItems();

        for (Employee employee : organization.getEmployeeDirectory().getEmployees()) {
            chooseEmpJComboBox.addItem(employee);
        }
    }

    private void populateRoleComboBox(Organization organization) {
        chooseRoleJComboBox.removeAllItems();

        for (Role role : organization.getSupportedRole()) {
            chooseRoleJComboBox.addItem(role);
        }
    }

