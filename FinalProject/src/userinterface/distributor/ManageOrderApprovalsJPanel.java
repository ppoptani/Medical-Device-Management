    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) pendingApprovalJTable.getModel();
        dtm.setRowCount(0);
        for (WorkRequest workRequest : enterprise.getWorkQueue().getWorkRequests()) {
            NewDeviceOrderRequest deviceOrderRequest = (NewDeviceOrderRequest) workRequest;

            Object[] rows = new Object[3];
            rows[0] = deviceOrderRequest;
            rows[1] = deviceOrderRequest.getRequestDate();
            rows[2] = deviceOrderRequest.getStatus();

            dtm.addRow(rows);

        }
    }

