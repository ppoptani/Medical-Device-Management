    private void rejectJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rejectJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = pendingApprovalJTable.getSelectedRow();

        if (pendingApprovalJTable.getSelectedRowCount() > 0) {

            NewDeviceOrderRequest request = (NewDeviceOrderRequest) pendingApprovalJTable.getValueAt(selectedRow, 0);

            if (request.getStatus().equals(WorkRequest.StatusType.Approved.getValue())) {
                JOptionPane.showMessageDialog(null, "Request already approved cannnot reject!!.");
                return;
            }

            if (request.getStatus().equals(WorkRequest.StatusType.Placed.getValue())) {
                JOptionPane.showMessageDialog(null, "Order placed. cannnot reject.");
                return;
            }

            request.setStatus(WorkRequest.StatusType.Reject.getValue());
            request.getOrder().setStatus(StatusType.Reject.getValue());
            JOptionPane.showMessageDialog(null, "Request rejected successsfully.");
            populateTable();
            
        } else {
            JOptionPane.showMessageDialog(null, "Select the request to be removed.");
        }
    }//GEN-LAST:event_rejectJButtonActionPerformed

    private void viewOrderDetailsJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewOrderDetailsJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = pendingApprovalJTable.getSelectedRow();

        if (pendingApprovalJTable.getSelectedRowCount() > 0) {
            NewDeviceOrderRequest request = (NewDeviceOrderRequest) pendingApprovalJTable.getValueAt(selectedRow, 0);

            ViewOrderDetailsJPanel viewOrderDetailsJPanel = new ViewOrderDetailsJPanel(userProcessContainer, enterprise, request);
            userProcessContainer.add("ViewOrderDetailsJPanel", viewOrderDetailsJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        } else {
            JOptionPane.showMessageDialog(null, "Select the request first.");
        }
    }//GEN-LAST:event_viewOrderDetailsJButtonActionPerformed

