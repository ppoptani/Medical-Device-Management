    private void populateSurgeryTable() {
        DefaultTableModel model = (DefaultTableModel) surgeryTypeJTable.getModel();
        model.setRowCount(0);

        for (SurgeryType surgeryType : enterprise.getSurgeryTypeCatalog().getSurgeryTypes()) {
            Object row[] = new Object[2];
            row[0] = surgeryType;
            row[1] = surgeryType.getTypeName();
            model.addRow(row);
        }
    }

